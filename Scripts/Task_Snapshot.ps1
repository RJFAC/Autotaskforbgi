    if (Test-Path $File.FullName) { 
        $RawContent = Get-Content $File.FullName -Raw -Encoding UTF8
        $CleanContent = Sanitize-Content $RawContent
        Add-Content -Path $CombinedConfigFile -Value $CleanContent -Encoding UTF8 
    }
}

